    private void btneliminarnotaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneliminarnotaActionPerformed
        int selectedRow = jTable1.getSelectedRow(); // Obtener la fila seleccionada
        if (selectedRow != -1) {
            // Confirmar antes de eliminar
            int confirm = JOptionPane.showConfirmDialog(this, "¿Está seguro de eliminar esta nota?", "Confirmar eliminación", JOptionPane.YES_NO_OPTION);
            if (confirm == JOptionPane.YES_OPTION) {
                // Eliminar la fila seleccionada
                model.removeRow(selectedRow);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Seleccione una fila para eliminar", "Error", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btneliminarnotaActionPerformed

    private void btneditarnotaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneditarnotaActionPerformed
        // TODO add your handling code here:
        int selectedRow = jTable1.getSelectedRow(); // Obtener la fila seleccionada
        if (selectedRow != -1) {
            // Obtener los nuevos valores de los campos
            String codNota = jTextField3.getText();
            String codEstudiante = jTextField2.getText();
            String calificacion = jTextField1.getText();
            String curso = jTextField4.getText();

            // Validar que los campos no estén vacíos
            if (codNota.isEmpty() || codEstudiante.isEmpty() || calificacion.isEmpty() || curso.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Por favor, complete todos los campos", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            // Actualizar la fila seleccionada
            model.setValueAt(codNota, selectedRow, 0);
            model.setValueAt(codEstudiante, selectedRow, 1);
            model.setValueAt(calificacion, selectedRow, 2);
            model.setValueAt(curso, selectedRow, 3);
        } else {
            JOptionPane.showMessageDialog(this, "Seleccione una fila para editar", "Error", JOptionPane.WARNING_MESSAGE);
        }
        
    }//GEN-LAST:event_btneditarnotaActionPerformed

    private void btnagregarnotaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnagregarnotaActionPerformed
    /// Obtener los valores de los campos de la vista del profesor
    String codNota = jTextField3.getText();  // Código de nota
    String codEstudiante = jTextField2.getText();  // Código de estudiante
    String calificacion = jTextField1.getText();  // Calificación
    String curso = jTextField4.getText();  // Curso

    // Verificar si los campos no están vacíos
    if (codNota.isEmpty() || codEstudiante.isEmpty() || calificacion.isEmpty() || curso.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Por favor, complete todos los campos", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    // Crear una nueva instancia de la clase Nota
    Nota nuevaNota = new Nota(codNota, codEstudiante, calificacion, curso);

    // Agregar la nota a la lista global de notas utilizando NotasController
    NotasController.agregarNota(nuevaNota);

    // Actualizar la tabla en la vista del profesor
    actualizarTablaNotas();
    }//GEN-LAST:event_btnagregarnotaActionPerformed

