private void actualizarTablaNotas() {
    // Obtener las notas desde el controlador
    List<Nota> listaNotas = NotasController.obtenerNotas();

    // Crear el modelo de la tabla (puede ser DefaultTableModel o uno personalizado)
    DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
    
    // Limpiar las filas actuales de la tabla
    model.setRowCount(0);

    // Recorrer la lista de notas y agregar cada una a la tabla
    for (Nota nota : listaNotas) {
        Object[] row = new Object[4]; // Suponiendo que la tabla tiene 4 columnas
        row[0] = nota.getCodNota();
        row[1] = nota.getCodEstudiante();
        row[2] = nota.getCalificacion();
        row[3] = nota.getCurso();
        
        model.addRow(row); // Agregar la fila a la tabla
    }}
