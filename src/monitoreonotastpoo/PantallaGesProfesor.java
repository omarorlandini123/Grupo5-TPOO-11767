    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        // Crear un nuevo Profesor usando los valores de los campos
        String nombre = txtNombreProf.getText();
        String apellido = txtApellProf.getText();
        String codigo = txtCodigoProf.getText();
        String curso = comboboxCurso.getSelectedItem().toString();

        // Crear el nuevo objeto Profesor
        Profesor nuevoProfesor = new Profesor(nombre, apellido, codigo, curso);

        // Agregar el profesor al modelo de lista
        modeloListaProfesores.addElement(nuevoProfesor);

        // Limpiar los campos
        txtNombreProf.setText("");
        txtApellProf.setText("");
        txtCodigoProf.setText("");
        comboboxCurso.setSelectedIndex(0);
        txtCorreoProf.setText("");
        txtTelefonoProf.setText("");
    }//GEN-LAST:event_btnAgregarActionPerformed

